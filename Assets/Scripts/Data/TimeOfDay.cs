/// <summary>
/// Time periods that affect resource availability and visual appearance
/// </summary>
public enum TimeOfDay
{
    Morning,   // 6-12: Dewdrops, early birds, nectar
    Afternoon, // 12-18: Seeds, berries, bugs, full sunlight
    Evening,   // 18-22: Fireflies, sunset pollen bloom
    Night      // 22-6: Moonbeams, stardust, falling stars
}