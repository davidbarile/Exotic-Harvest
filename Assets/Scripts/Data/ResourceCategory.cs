/// <summary>
/// Categories for organizing resources in UI and systems
/// </summary>
public enum ResourceCategory
{
    Primary,     // Water and basic resources
    Bugs,        // All insects and creatures
    Nature,      // Plants, seeds, natural items
    NightSky,    // Celestial and night-time resources
    Valuables,   // Gems, gold, precious items
    Abstract,    // Secrets, memories, intangible resources
    Special,     // Rare event resources
    Premium      // Hard currency
}